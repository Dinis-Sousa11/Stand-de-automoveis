def loading_bar(texto):
    print(Fore.WHITE + texto, end="")
    for _ in range(20):
        time.sleep(0.05)
        sys.stdout.write(Fore.GREEN + "█")
        sys.stdout.flush()
    print(Style.RESET_ALL + " OK!")


